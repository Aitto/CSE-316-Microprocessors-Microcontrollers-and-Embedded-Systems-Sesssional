.MODEL SMALL
.STACK 100H
.DATA

MSG1 DB '   GREATER$'
MSG2 DB '   LESSER$'

.CODE

MAIN PROC
    
    
    MOV AH,2
    MOV CX,26 ;CX IS N. LOOP NUMBER. THIS TIME IT WILL RUN 26 TIMES 
    ;IF CX IS INITAILLY 0 THEN IT RUNS ADDITIONAL 65535 TIMES
    MOV DL,'A'
    
    TOP:
    
    INT 21H
    INC DL
    
    LOOP TOP
    
    
    
    MOV AH,4CH
    INT 21H
    MAIN ENDP
END MAIN
